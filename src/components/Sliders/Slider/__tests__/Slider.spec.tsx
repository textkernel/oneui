import React from 'react';
import { render, screen } from '@testing-library/react';
import '@testing-library/jest-dom';
import { Slider } from '../Slider';

describe('Slider component', () => {
    const defaultAriaMax = '100';
    const defaultAriaMin = '0';
    const defaultAriaDisabled = 'false';
    const defaultAriaNow = '0';

    it('should render component correctly', () => {
        const view = render(<Slider />);
        const slider = screen.getByRole('slider');

        expect(view.container).toMatchSnapshot();
        expect(slider).toBeInTheDocument();
        expect(slider).toHaveAttribute('aria-valuemax', defaultAriaMax);
        expect(slider).toHaveAttribute('aria-valuemin', defaultAriaMin);
        expect(slider).toHaveAttribute('aria-disabled', defaultAriaDisabled);
        expect(slider).toHaveAttribute('aria-valuenow', defaultAriaNow);
    });
});
